int main(void)
{
    int i;
    int digs, tens;
    int ret = 0;
    int dma_channel;
    int slaveAddress;
    int id;

    // Wait for PMIC 1.8V to become available, about 180ms after power up.
    MXC_Delay(200000);

    /* Enable camera power */
    Camera_Power(POWER_ON);

    MXC_ICC_Enable(MXC_ICC0); // Enable cache

    // Switch to 100 MHz clock
    MXC_SYS_Clock_Select(MXC_SYS_CLOCK_IPO);
    SystemCoreClockUpdate();

    // Enable peripheral, enable CNN interrupt, turn on CNN clock
    // CNN clock: APB (50 MHz) div 1
    cnn_enable(MXC_S_GCR_PCLKDIV_CNNCLKSEL_PCLK,
               MXC_S_GCR_PCLKDIV_CNNCLKDIV_DIV1);

    /* Configure P2.5, turn on the CNN Boost */
    cnn_boost_enable(MXC_GPIO2, MXC_GPIO_PIN_5);

    cnn_init();          // Bring state machine into consistent state
    cnn_load_weights();  // Load kernels
    cnn_load_bias();
    cnn_configure();     // Configure state machine

    // Initialize fused risk monitor
    // Threshold set to 0.15 so score > 0.15 triggers, as requested.
    RiskMonitor_Init(&g_risk_monitor,
                     0.15f,  // threshold
                     0.6f,   // eye_weight
                     0.2f,   // hr_weight (minimal influence for now)
                     0.2f,   // accel_weight (minimal influence for now)
                     0.0f,
                     70.0f,
                     1.0f);

    // Initialize DMA for camera interface
    MXC_DMA_Init();
    dma_channel = MXC_DMA_AcquireChannel();

    // Make the console UART available globally
    ConsoleUart = MXC_UART_GET_UART(CONSOLE_UART);

    if ((ret = MXC_UART_Init(ConsoleUart, CON_BAUD, MXC_UART_IBRO_CLK)) != E_NO_ERROR) {
        return ret;
    }

    // Initialize camera.
    printf("Init Camera.\n");
    camera_init(CAMERA_FREQ);

    slaveAddress = camera_get_slave_address();
    printf("Camera I2C slave address: %02x\n", slaveAddress);

    // Obtain product ID of the camera.
    ret = camera_get_product_id(&id);

    if (ret != STATUS_OK) {
        printf("Error returned from reading camera id. Error %d\n", ret);
        return -1;
    }

    printf("Camera ID detected: %04x\n", id);

    ret = camera_setup(IMAGE_SIZE_X, IMAGE_SIZE_Y,
                       PIXFORMAT_RGB888, FIFO_THREE_BYTE,
                       STREAMING_DMA, dma_channel);

    if (ret != STATUS_OK) {
        printf("Error returned from setting up camera. Error %d\n", ret);
        return -1;
    }

    MXC_Delay(SEC(1));
    camera_write_reg(0x11, 0x0);

    // Init haptic GPIO (DFR0440)
    Haptic_Init();

    printf("********** Press '\\' on your PC console to start 2-second eye window **********\r\n");
    printf("Sampling at ~0.5 s per frame (%d frames per window).\r\n", EYE_WINDOW_SIZE);

    /* Enable CNN clock */
    MXC_SYS_ClockEnable(MXC_SYS_PERIPH_CLOCK_CNN);

    // ______________________________________________________________________________________________
    // Move ADXL345 device handle to global so PollAccel can read it at any time
    struct adxl345_init_param init_param;
    int32_t status = 0;
    
    // Acceleration readings are now global (g_x_g, g_y_g, etc.)

    printf("\n***** MAX78000 ADXL345 I2C Demo (continuous polling) *****\n");

    // Initialize parameters for the ADXL345 driver
    init_param.i2c_inst = SENSOR_I2C_PORT;
    init_param.i2c_addr = ADXL345_ADDR;
    init_param.dev_type = ID_ADXL345;
    
    // Set preferred range and resolution (optional, defaults to +/- 2g, 10-bit)
    init_param.selected_range = ADXL345_RANGE_PM_8G; // Set to +/- 8g range
    init_param.full_resolution_set = ADXL345_FULL_RES; // Enable Full Resolution (13-bit)

    // 1. Initialize the ADXL345 device (initializes I2C and checks device ID)
    status = adxl345_init(&g_adxl_dev, init_param);
    
    if (status != 0) {
        printf("ERROR: ADXL345 Initialization Failed! (Status: %ld)\n", status);
        printf("Check I2C connections and slave address (0x%X).\n", ADXL345_ADDR);
        MXC_Delay(MXC_DELAY_SEC(5));
        return 1;
    }
    
    printf("ADXL345 initialized successfully.\n");
    
    // 2. Configure the sensor's measurement properties
    
    // Set range and resolution
    adxl345_set_range_resolution(g_adxl_dev, 
                                 init_param.selected_range, 
                                 init_param.full_resolution_set);
    
    // Put the device in Measure Mode (continuous polling)
    adxl345_set_power_mode(g_adxl_dev, 1);
    printf("ADXL345 set to Measure Mode.\n");

    // Immediately sample once to prime the globals
    //PollAccel();

    while (1) {
        // Wait for '\' key from PC to start a new 2-second window
        printf("\r\n[RiskMonitor] Waiting for '\\' to start 2-second eye window...\r\n");
        // Poll the accelerometer continuously while idle so values stay fresh
        while (!check_for_backslash()) {
            //PollAccel();
            // Small sleep so this loop doesn't busy-wait too hard
            MXC_Delay(MSEC(10));
        }

        printf("\r\n[RiskMonitor] Starting 2-second eye window...\r\n");

        // Collect EYE_WINDOW_SIZE frames over approximately 2 seconds
        for (int sample_idx = 0; sample_idx < EYE_WINDOW_SIZE; ++sample_idx) {
            LED_Off(LED1);
            LED_Off(LED2);

            // Capture image from camera into input_0[]
            capture_process_camera();

            // Run CNN on captured image
            cnn_time = 0;
            cnn_start();
            load_input();

            // Wait for CNN
            while (cnn_time == 0) {
                MXC_LP_EnterSleepMode();
            }

            // Unload CNN data and stop clock
            cnn_unload((uint32_t *)ml_data);
            cnn_stop();

            // Softmax
            softmax_q17p14_q15((const q31_t *)ml_data,
                               CNN_NUM_OUTPUTS, ml_softmax);

            // Eye-closed confidence for "Closed" class (index 0) as 0â€“100%
            float eye_closed_conf_pct = 0.0f;
            if (CNN_NUM_OUTPUTS >= 1) {
                eye_closed_conf_pct =
                    ((float)ml_softmax[0] * 100.0f) / 32768.0f;
                if (eye_closed_conf_pct < 0.0f)  eye_closed_conf_pct = 0.0f;
                if (eye_closed_conf_pct > 100.0f) eye_closed_conf_pct = 100.0f;
            }

            // --- read accelerometer now and compute weighted XZ value ---
            // Use the latest polled value so reading is effectively continuous.
            // But also poll now to get the freshest sample at inference time.
            //PollAccel(); // refresh before computing/federating into RiskMonitor
            //float eye_accel_xz = g_final_g_xz;

            // Update RiskMonitor with real accel (final_g_xz) instead of 0.0f
            bool alert = RiskMonitor_Update(&g_risk_monitor,
                                            eye_closed_conf_pct,
                                            0.0f, 0.7f);       // TODO: real HR ROC (BPM/s)
                                            //eye_accel_xz); // <-- pass weighted XZ g here

            float score = RiskMonitor_GetScore(&g_risk_monitor);

            // Print the sample, include accel XZ on the same line
            printf("Sample %d/%d: eye_closed_conf=%.1f%%, score=%.3f, accel_xz=%.3f g, alert=%d\r\n",
                   sample_idx + 1, EYE_WINDOW_SIZE,
                   eye_closed_conf_pct, score, 0.7f, alert ? 1 : 0);

            // Per-0.5s BUZZ / no buzz + haptic drive
            if (alert) {
                printf("BUZZ\r\n");
                Haptic_Set(true);
            } else {
                printf("no buzz\r\n");
                Haptic_Set(false);
            }

            // Re-pack last grayscale image into RGB888 buffer for PC download
            int count = 0;
            for (int pix = 0; pix < (int)(lens / 4); ++pix) {
                image_buffer[count++] =
                    (uint8_t)((input_0[pix] >> 16) & 0xFF);
                image_buffer[count++] =
                    (uint8_t)((input_0[pix] >> 8) & 0xFF);
                image_buffer[count++] =
                    (uint8_t)(input_0[pix] & 0xFF);
            }

            // Delay to roughly hit 0.5 s per frame, ignoring processing time
            if (sample_idx < (EYE_WINDOW_SIZE - 1)) {
                MXC_Delay(SEC(1) / 2); // ~0.5 s
            }
        } // end 2-second window loop

        float final_score = RiskMonitor_GetScore(&g_risk_monitor);
        bool  final_alert = RiskMonitor_GetAlertFlag(&g_risk_monitor);

        printf("\r\n[RiskMonitor] 2-second window complete.\r\n");
        printf("RiskMonitor: score=%.3f, alert=%d\r\n",
               final_score, final_alert ? 1 : 0);

        if (final_alert) {
            printf("BUZZ\r\n");
            Haptic_Set(true);
            MXC_Delay(SEC(2)); // Buzz for 1 second
            Haptic_Set(false);
        } else {
            printf("no buzz\r\n");
            Haptic_Set(false);
        }

        printf("Approximate data loading and inference time (last frame): %u us\r\n\n",
               cnn_time);

        printf("Classification results (last frame in window):\r\n");
        for (i = 0; i < CNN_NUM_OUTPUTS; i++) {
            digs = (1000 * ml_softmax[i] + 0x4000) >> 15;
            tens = digs % 10;
            digs = digs / 10;
            printf("[%7d] -> Class %d %8s: %d.%d%%\r\n",
                   ml_data[i], i, classes[i], digs, tens);
        }

        printf("\n");
        printf("********** Press '\\' on your PC console to start a new 2-second window **********\r\n");
    }

    return 0;
}

