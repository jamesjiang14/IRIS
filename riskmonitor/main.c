void capture_process_camera(void)
{
    uint8_t *raw;
    uint32_t imgLen;
    uint32_t w, h;

    int cnt = 0;

    uint8_t r, g, b;
    uint8_t *data = NULL;
    stream_stat_t *stat;

    camera_start_capture_image();

    // Get the details of the image from the camera driver.
    camera_get_image(&raw, &imgLen, &w, &h);

    printf("W:%d H:%d L:%d \n", w, h, imgLen);

    lens = imgLen;
    ws   = w;
    hs   = h;

    for (int row = 0; row < (int)h; row++) {
        // Wait until camera streaming buffer is full
        while ((data = get_camera_stream_buffer()) == NULL) {
            if (camera_is_image_rcv()) {
                break;
            }
        }

        for (int k = 0; k < 4 * (int)w; k += 4) {
            // data format: 0x00bbggrr
            r = data[k];
            g = data[k + 1];
            b = data[k + 2];
            // skip k+3

            uint8_t l = (uint8_t)(0.299f * r + 0.587f * g + 0.114f * b);

            // change the range from [0,255] to [-128,127] and store in buffer for CNN
            input_0[cnt++] = ((l << 16) | (l << 8) | l) ^ 0x00808080;
        }

        release_camera_stream_buffer();
    }

    stat = get_camera_stream_statistic();

    if (stat->overflow_count > 0) {
        printf("OVERFLOW DISP = %d\n", stat->overflow_count);
        LED_On(LED2); // Turn on red LED if overflow detected
        while (1) {}
    }
}

// -------------------- Serial helper --------------------
// Non-blocking check for a backslash key '\' from the host console.
// Returns 1 if backslash was received (and consumes the byte), 0 otherwise.
static int check_for_backslash(void)
{
    int ch;

    if (ConsoleUart == NULL) {
        return 0;
    }

    // MXC_UART_ReadCharacterRaw returns character (>=0) or error (<0) when none.
    ch = MXC_UART_ReadCharacterRaw(ConsoleUart);
    if (ch >= 0) {
        if ((char)ch == '\\') {
            return 1;
        }
        // If you want to echo or handle other keys you can do that here.
    }
    return 0;
}
