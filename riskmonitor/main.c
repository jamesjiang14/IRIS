// -------------------- ADXL345 continuous polling support --------------------
// Make the ADXL device and the latest accel values global so we can poll from
// idle loops and keep values fresh even when waiting for keypresses.
static struct adxl345_dev *g_adxl_dev = NULL;
static float g_x_g = 0.0f, g_y_g = 0.0f, g_z_g = 0.0f;
static float g_gmag = 0.0f;
static float g_final_g_xz = 0.0f;

// Poll accelerometer once (non-blocking in sense of other code; the driver call may be blocking briefly)
static void PollAccel(void)
{
    if (g_adxl_dev == NULL) return;

    // read xyz from ADXL345 and update globals
    adxl345_get_g_xyz(g_adxl_dev, &g_x_g, &g_y_g, &g_z_g);
    g_gmag = gforce_mag_from_xyz(g_x_g, g_y_g, g_z_g);
    g_final_g_xz = gforce_weighted_from_xz(g_x_g, g_z_g);
}

